class Person {
  int id;
  String name;
  String lastName;
  String telephone;
  String email;
  String cedula;
  bool isDoctor;

  Person({
    required this.id,
    required this.name,
    required this.lastName,
    required this.telephone,
    required this.email,
    required this.cedula,
    required this.isDoctor,
  });
}
